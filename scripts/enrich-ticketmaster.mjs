#!/usr/bin/env node
/**
 * Enriches the canonical (Wikidata) festival registry with live data from the
 * Ticketmaster Discovery API: dates, day-by-day lineup, ticket links, and
 * backfilled city/image where missing. Matches by festival name + location,
 * so it never adds Ticketmaster's noisy names as new rows.
 *
 * Usage:
 *   TM_API_KEY=your_key node --env-file=.env.local scripts/enrich-ticketmaster.mjs
 *
 * Reads Supabase read-only (publishable key) and writes supabase/seed_enrichment.sql
 * for you to run in the SQL editor.
 */

import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const TM_KEY = process.env.TM_API_KEY;
const SB_URL = process.env.NEXT_PUBLIC_SUPABASE_URL;
const SB_KEY = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;
if (!TM_KEY || !SB_URL || !SB_KEY) {
  console.error("Need TM_API_KEY (env) and NEXT_PUBLIC_SUPABASE_URL/ANON_KEY (--env-file=.env.local)");
  process.exit(1);
}

const TM_BASE = "https://app.ticketmaster.com/discovery/v2/events.json";

function normalize(s) {
  return (s || "")
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/\b(festival|festivalen|fest|open air|openair|open-air)\b/g, " ")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

async function getFestivals() {
  const url =
    `${SB_URL}/rest/v1/festivals?select=slug,name,latitude,longitude,city,image_url` +
    `&source=eq.wikidata&limit=2000`;
  const res = await fetch(url, { headers: { apikey: SB_KEY, Authorization: `Bearer ${SB_KEY}` } });
  if (!res.ok) throw new Error(`Supabase ${res.status}`);
  return res.json();
}

async function searchTm(festival) {
  const core = normalize(festival.name) || festival.name.toLowerCase();
  const url =
    `${TM_BASE}?apikey=${TM_KEY}&classificationName=music&size=60&locale=*` +
    `&keyword=${encodeURIComponent(core)}` +
    `&latlong=${festival.latitude},${festival.longitude}&radius=60&unit=km`;
  const res = await fetch(url);
  if (res.status === 429) {
    await new Promise((r) => setTimeout(r, 2000));
    return searchTm(festival);
  }
  if (!res.ok) return [];
  const data = await res.json();
  return data?._embedded?.events ?? [];
}

const festivals = await getFestivals();
console.error(`Enriching ${festivals.length} Wikidata festivals…`);

const enriched = []; // { slug, dates: Map(date->Set), ticketUrl, city, image }
let matched = 0;

for (let i = 0; i < festivals.length; i++) {
  const f = festivals[i];
  if (i % 50 === 0) process.stderr.write(`  ${i}/${festivals.length} (${matched} matched)\n`);

  let events = [];
  try {
    events = await searchTm(f);
  } catch {
    /* skip on error */
  }
  await new Promise((r) => setTimeout(r, 250)); // ~4 req/s

  const core = normalize(f.name);
  if (!core) continue;

  const dates = new Map();
  let ticketUrl = null;
  let city = null;
  let image = null;

  for (const ev of events) {
    const evNorm = normalize(ev.name);
    // Short cores (e.g. "a" from "A Festival") only match on exact equality to
    // avoid false positives; longer cores may match as a substring.
    const isMatch =
      evNorm === core || (core.length >= 5 && (evNorm.includes(core) || core.includes(evNorm)));
    if (!isMatch) continue;
    const venue = ev._embedded?.venues?.[0];
    const date = ev.dates?.start?.localDate ?? null;
    if (!date) continue;

    if (!dates.has(date)) dates.set(date, new Set());
    // Skip "attractions" that are just the festival itself (pass listings).
    (ev._embedded?.attractions || []).forEach((a) => {
      if (a.name && normalize(a.name) !== core) dates.get(date).add(a.name);
    });

    if (!ticketUrl && ev.url) ticketUrl = ev.url;
    if (!city && venue?.city?.name) city = venue.city.name.trim();
    if (!image) {
      image =
        (ev.images || []).filter((im) => im.width >= 640).sort((a, b) => b.width - a.width)[0]?.url ??
        null;
    }
  }

  if (dates.size === 0) continue;
  matched++;
  enriched.push({
    slug: f.slug,
    dates,
    ticketUrl,
    city: f.city ? null : city, // only backfill when missing
    image: f.image_url ? null : image,
  });
}

console.error(`\nMatched ${matched}/${festivals.length} festivals with live Ticketmaster data.`);

const esc = (v) => String(v).replace(/'/g, "''");
const sqlStr = (v) => (v == null || v === "" ? "null" : `'${esc(v)}'`);

let sql = `-- Auto-generated by scripts/enrich-ticketmaster.mjs\n`;
sql += `-- Enriched ${matched} festivals with dates/lineup/tickets from Ticketmaster.\n\n`;

// festival_dates
const dateRows = [];
for (const f of enriched) for (const d of f.dates.keys()) dateRows.push([f.slug, d]);
if (dateRows.length) {
  sql += `insert into festival_dates (festival_id, date)\nselect fest.id, v.date::date from (values\n`;
  sql += dateRows.map(([slug, d]) => `  (${sqlStr(slug)}, ${sqlStr(d)})`).join(",\n");
  sql += `\n) as v(slug, date)\njoin festivals fest on fest.slug = v.slug\non conflict (festival_id, date) do nothing;\n\n`;
}

// artists
const artists = new Set();
for (const f of enriched) for (const set of f.dates.values()) for (const a of set) artists.add(a);
if (artists.size) {
  sql += `insert into artists (name) values\n`;
  sql += [...artists].map((a) => `  (${sqlStr(a)})`).join(",\n");
  sql += `\non conflict (name) do nothing;\n\n`;
}

// performances
const perfRows = [];
for (const f of enriched)
  for (const [d, set] of f.dates) for (const a of set) perfRows.push([f.slug, d, a]);
if (perfRows.length) {
  sql += `insert into performances (festival_date_id, artist_id)\nselect fd.id, ar.id from (values\n`;
  sql += perfRows.map(([slug, d, a]) => `  (${sqlStr(slug)}, ${sqlStr(d)}, ${sqlStr(a)})`).join(",\n");
  sql += `\n) as v(slug, date, artist_name)
join festivals fest on fest.slug = v.slug
join festival_dates fd on fd.festival_id = fest.id and fd.date = v.date::date
join artists ar on ar.name = v.artist_name
on conflict (festival_date_id, artist_id, stage) do nothing;\n\n`;
}

// ticket links (only if the festival has no Ticketmaster link yet)
const ticketRows = enriched.filter((f) => f.ticketUrl);
if (ticketRows.length) {
  sql += `insert into ticket_links (festival_id, provider, url)\nselect fest.id, 'Ticketmaster', v.url from (values\n`;
  sql += ticketRows.map((f) => `  (${sqlStr(f.slug)}, ${sqlStr(f.ticketUrl)})`).join(",\n");
  sql += `\n) as v(slug, url)
join festivals fest on fest.slug = v.slug
where not exists (
  select 1 from ticket_links tl where tl.festival_id = fest.id and tl.provider = 'Ticketmaster'
);\n\n`;
}

// backfill city / image where missing
const backfill = enriched.filter((f) => f.city || f.image);
if (backfill.length) {
  sql += `update festivals fest set\n  city = coalesce(fest.city, v.city),\n  image_url = coalesce(fest.image_url, v.image)\nfrom (values\n`;
  sql += backfill
    .map((f) => `  (${sqlStr(f.slug)}, ${sqlStr(f.city)}, ${sqlStr(f.image)})`)
    .join(",\n");
  sql += `\n) as v(slug, city, image)\nwhere fest.slug = v.slug;\n`;
}

const outPath = join(dirname(fileURLToPath(import.meta.url)), "..", "supabase", "seed_enrichment.sql");
writeFileSync(outPath, sql);
console.error(
  `Wrote enrichment for ${matched} festivals (${dateRows.length} dates, ${artists.size} artists) to supabase/seed_enrichment.sql`,
);
